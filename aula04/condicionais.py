print('Trabalhando com Condicionais')

numero = float(input('Entre com um valor: '))

if numero > 10:
    print(f'O número {numero} é maior que 10')
else:
    print(f'O número {numero} é menor que 10 ')

## Operadores relacionais
# Maior: >
# Menor: <
# Maior ou Igual: >=
# Menor ou Igual: <=
# Igualdade: ==
